pub const PublicKey = struct {
    modulus: [128]u8,
    exponent: [128]u8,

    pub fn encrypt(key: *const PublicKey, data: []const u8, out: *[block_size]u8) void {
        assert(data.len <= max_unpadded_size);

        out[0] = 0x00;
        out[1] = 0x02;

        for (out[2..][0 .. out.len - data.len - 3]) |*padding|
            padding.* = 0xFF; // TODO: this can be random

        out[2 + block_size - data.len - 3] = 0x00; // termination

        @memcpy(out[out.len - data.len ..][0..data.len], data);

        const modulus = Modulus.fromBytes(&key.modulus, .big) catch unreachable;
        const exponent = Modulus.Fe.fromBytes(modulus, &key.exponent, .big) catch unreachable;

        const int = Modulus.Fe.fromBytes(modulus, out, .big) catch unreachable;
        const ciphertext = modulus.powPublic(int, exponent) catch unreachable;
        ciphertext.toBytes(out, .big) catch unreachable;
    }
};

pub const PrivateKey = struct {
    modulus: [128]u8,
    private_exponent: [128]u8,

    pub fn sign(key: *const PrivateKey, data: []const u8, out: *[block_size]u8) void {
        out[0] = 0x00;
        out[1] = 0x01;

        const padding_end = block_size - sign_oid.len - hash_size - 1;
        for (out[2..padding_end]) |*padding|
            padding.* = 0xFF;

        out[padding_end] = 0x00;
        @memcpy(out[padding_end + 1 ..][0..sign_oid.len], sign_oid);
        std.crypto.hash.sha2.Sha256.hash(data, out[padding_end + 1 + sign_oid.len ..], .{});

        const modulus = Modulus.fromBytes(&key.modulus, .big) catch unreachable;
        const exponent = Modulus.Fe.fromBytes(modulus, &key.private_exponent, .big) catch unreachable;

        const int = Modulus.Fe.fromBytes(modulus, out, .big) catch unreachable;
        const ciphertext = modulus.pow(int, exponent) catch unreachable;
        ciphertext.toBytes(out, .big) catch unreachable;
    }

    pub fn decrypt(key: *const PrivateKey, ciphertext: *const [block_size]u8, out: *[block_size]u8) ?[]const u8 {
        const modulus = Modulus.fromBytes(&key.modulus, .big) catch unreachable;
        const exponent = Modulus.Fe.fromBytes(modulus, &key.private_exponent, .big) catch unreachable;

        const int = Modulus.Fe.fromBytes(modulus, ciphertext, .big) catch unreachable;
        const plaintext = modulus.pow(int, exponent) catch unreachable;
        plaintext.toBytes(out, .big) catch unreachable;

        const padding_end = 3 + (std.mem.findScalar(u8, out[2..], 0) orelse return null);
        return out[padding_end..];
    }
};

